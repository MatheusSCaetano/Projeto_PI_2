    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        //System.out.println("Código da venda:"+codvenda);testes
        //System.out.println("Codigo do produto:"+labelcod.getText());testes
        //System.out.println("ESTOQUE:"+labelestoque.getText());testes
       if(labelestoque.getText().equals("jLabel10") || txtCPF.getText().equals("") ){
           JOptionPane.showMessageDialog(this,"Para realizar o processo de venda, é necessario um cliente e um produto selecionado. Por favor Confira se os dois estao selecionados");
       }
       
       else{
     // verificando se o produto tem estoque para realizar venda
     if(Integer.parseInt(labelestoque.getText())>=Integer.parseInt(txtQuantidade.getText()))
     {
     //se permitiu a venda realizar o decremento e armazenas na variavel para decrementarmos diretamento do BD
     int estoque_sobrou=Integer.parseInt(labelestoque.getText()) - Integer.parseInt(txtQuantidade.getText());
     //adicionando item no carrinho
    // atulizo os onjetos da classe Venda para conseguir realizar a diacao do produto no carrinho
      Venda venda = new Venda();
      venda.setCpfCliente(txtCPF.getText().replace(".", "").replace("-", ""));
      venda.setNomeProduto(txtProduto.getText());
      venda.setQtdItem(Integer.parseInt(txtQuantidade.getText()));
      venda.setCodProduto(Integer.parseInt(labelcod.getText()));
      venda.setIdVenda(codvenda);
      venda.setEstoque(estoque_sobrou);
     
      boolean retorno = VendaController.Criandoitemporvenda(venda);
      
        if (retorno == true){
            // atualizar a planilha de pesquisa
            ArrayList<Produto> listaProduto = ProdutoDAO.consultarProdutopornome(txtProduto.getText());

        DefaultTableModel tmPesquisar = new DefaultTableModel()
                         {
                    @Override
                    public boolean isCellEditable(final int row, final int column) {
                            return false;
                    }
            };
        tmPesquisar.addColumn("Id_Produto");
        tmPesquisar.addColumn("Nome");
        tmPesquisar.addColumn("Valor");
        tmPesquisar.addColumn("Qntd. Estoque");
       

        tblPesquisa.setModel(tmPesquisar);

        //Limpo a tabela, excluindo todas as linhas para depois mostrar os dados novamente
        tmPesquisar.setRowCount(0);

        //Para cada produto resgatado do banco de dados, atualizo a tabela
        for (Produto p : listaProduto) {
            tmPesquisar.addRow(new Object[]{p.getIdProduto(),p.getProduto(),p.getPreco(), p.getEstoque()});
        }
        //Defino o tamanho para cada coluna
       
        tblPesquisa.getColumnModel().getColumn(0).setPreferredWidth(70);
        tblPesquisa.getColumnModel().getColumn(1).setPreferredWidth(100);
        tblPesquisa.getColumnModel().getColumn(2).setPreferredWidth(100);
        tblPesquisa.getColumnModel().getColumn(3).setPreferredWidth(100);
            
        }
        
        else{
            JOptionPane.showMessageDialog(null,"Falha ao colocar item no carrinho!");
        }
        
       
       //adicionar no carrinho
       ArrayList<Venda> carregaritensvenda = VendaDAO.Carregaritensvenda(codvenda);
        
        DefaultTableModel tmCarrinho = new DefaultTableModel()
                         {
                    @Override
                    public boolean isCellEditable(final int row, final int column) {
                            return false;
                    }
            };
        tmCarrinho.addColumn("Produto");
        tmCarrinho.addColumn("Quantidade");
        tmCarrinho.addColumn("Total");
        
        

        tblCarrinho.setModel(tmCarrinho);

        
        tmCarrinho.setRowCount(0);
        
        for (Venda v : carregaritensvenda) {
            int quantidadeaservendida = v.getQtdItem();
            double valordoproduto = v.getvalorunitario();
            
            tmCarrinho.addRow(new Object[]{v.getNomeProduto(),v.getQtdItem(),""+quantidadeaservendida*valordoproduto});
        }
        //Defino o tamanho para cada coluna
       
        tblCarrinho.getColumnModel().getColumn(0).setPreferredWidth(70);
        tblCarrinho.getColumnModel().getColumn(1).setPreferredWidth(100);
        tblCarrinho.getColumnModel().getColumn(2).setPreferredWidth(100);
        
        
        
        
        
        double total = VendaDAO.Realizarsoma(codvenda);
        txtValorTotal.setText(Double.toString(total));
      }
     else{
         JOptionPane.showMessageDialog(null,"Produto com estoque insuficiente!");
     }
     
     }
    }//GEN-LAST:event_jButton3ActionPerformed
 
