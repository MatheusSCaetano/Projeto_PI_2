    private void btnbuscacpfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscacpfActionPerformed
        flag = 1; // atualizo a variavel flag para conseguir atualizar os campos e as labels
        if(txtCPF.getText().equals("   .   .   -  ")){
            JOptionPane.showMessageDialog(this,"Digite um CPF para realizar a busca");
        }
        else{
        String cpfnormal = txtCPF.getText().replace(".", "").replace("-", ""); // pego o que estiver mo campo txtCPF, apenas numeros
        
        ArrayList<Cliente> listaCliente = ClienteDAO.consultarClienteporCPF(cpfnormal);// chamo metodo diretamente da DAO que realiza pesquisa e retorna a lista atualizada referente a pesquisa
       if(listaCliente.size()>0){
        // crio um novo modelo de tabela
        DefaultTableModel tmPesquisa = new DefaultTableModel()
                         {
                    @Override
                    public boolean isCellEditable(final int row, final int column) {
                            return false;
                    }
            };
        tmPesquisa.addColumn("Id_Cliente");
        tmPesquisa.addColumn("Nome");
        tmPesquisa.addColumn("CPF");
        
        
        //Adiciono este novo modelo a tabela pesquisa
        tblPesquisa.setModel(tmPesquisa);


        //Limpo a tabela, excluindo todas as linhas para depois mostrar os dados novamente
        tmPesquisa.setRowCount(0);

        //Para cada cliente resgatado do banco de dados, atualizo a tabela
        for (Cliente c : listaCliente) {
            tmPesquisa.addRow(new Object[]{c.getIdCliente(),c.getNome(),c.getCPFSomenteNumeros()});
        }
        //Defino o tamanho para cada coluna
        tblPesquisa.getColumnModel().getColumn(0).setPreferredWidth(70);
        tblPesquisa.getColumnModel().getColumn(1).setPreferredWidth(100);
        tblPesquisa.getColumnModel().getColumn(2).setPreferredWidth(100);
       }else{
          JOptionPane.showMessageDialog(this,"Cliente não encontrado");
       }
       }  
    }//GEN-LAST:event_btnbuscacpfActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         
      Venda venda = new Venda(); // instancio um novo obejto da classeModel -> venda
      venda.setData(txtData.getText()); // Atualizo os parametros desta classe ->Data
      venda.setValorVenda(Double.parseDouble(txtValorTotal.getText()));// Atualizo os parametros desta classe ->Valor total da Venda
      venda.setCpfCliente(txtCPF.getText().replace(".", "").replace("-", ""));// Atualizo os parametros desta classe ->Cpf do cliente
      venda.setIdVenda(codvenda);// Atualizo o codigo da venda para conseguir realizar o cadstro da mesma
      
     
      boolean retorno = VendaController.PopulandoVenda(venda); // chamo umdos metodos do pacote Controller que serve para adicionar os itens na venda
      if(retorno==true){
          JOptionPane.showMessageDialog(null,"Venda Realizada com Sucesso!");//feedback
          dispose();//apos a venda ser cadastrada em sistema, fechar o formulario
      }
      else{
          JOptionPane.showMessageDialog(null,"Falha ao finalizar Venda!");//feedback
      }
    }//GEN-LAST:event_jButton1ActionPerformed

