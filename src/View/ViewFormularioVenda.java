        flag = 2;
        rodarvenda=rodarvenda+1;
        
        if(rodarvenda<2){
        retorno = VendaController.Criandovenda(0);
        if (retorno != 0){
            System.out.println("Venda aberta com sucesso!");
        }
        
        else{
            System.out.println("Falha ao abrir venda!");
        }
        }
        
        
        codvenda = retorno;
        if(txtProduto.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Digite o nome do produto para realizar a pesquisa");
        }
        else{
        ArrayList<Produto> listaProduto = ProdutoDAO.consultarProdutopornome(txtProduto.getText());
        if(listaProduto.size()>0){
        DefaultTableModel tmPesquisar = new DefaultTableModel()
                         {
                    @Override
                    public boolean isCellEditable(final int row, final int column) {
                            return false;
                    }
            };
        tmPesquisar.addColumn("Id_Produto");
        tmPesquisar.addColumn("Nome");
        tmPesquisar.addColumn("Valor");
        tmPesquisar.addColumn("Qntd. Estoque");
       

        tblPesquisa.setModel(tmPesquisar);

        //Removo a coluna ID da View (JTable) mas mantenho na model para armazenar o ID
        // tblMaquina.removeColumn(tblMaquina.getColumnModel().getColumn(0));

        //Limpo a tabela, excluindo todas as linhas para depois mostrar os dados novamente
        tmPesquisar.setRowCount(0);

        //Para cada cliente resgatado do banco de dados, atualizo a tabela
        for (Produto p : listaProduto) {
            tmPesquisar.addRow(new Object[]{p.getIdProduto(),p.getProduto(),p.getPreco(), p.getEstoque()});
        }
        //Defino o tamanho para cada coluna
        //tblClientes.getColumnModel().getColumn(0).setPreferredWidth(50); //ID
        tblPesquisa.getColumnModel().getColumn(0).setPreferredWidth(70);
        tblPesquisa.getColumnModel().getColumn(1).setPreferredWidth(100);
        tblPesquisa.getColumnModel().getColumn(2).setPreferredWidth(100);
        tblPesquisa.getColumnModel().getColumn(3).setPreferredWidth(100);
        } else{
            JOptionPane.showMessageDialog(this,"Produto não encontrado");
        }
       }  
